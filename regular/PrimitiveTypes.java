package regular;

public class PrimitiveTypes {

    public static void main(String[] args) {

        byte b = 10; // 1 byte
        short s = 20; // 2 bytes
        int i = 30; // 4 bytes
        long l = 40; // 8 bytes

        float f = 50.0f; // 4 bytes // single precision float
        double d = 60.0; // 8 bytes // double precision float

        char c = 'A'; // 2 bytes // UTF-16 character

        boolean bool = true; // 1 bit
        
    }
    
}
